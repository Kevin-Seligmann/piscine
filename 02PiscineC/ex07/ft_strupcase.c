char	*ft_strupcase(char *str)
{
	char	*ptr_str;

	ptr_str = str;
	while (*str != '\0')
	{
		if (*str > 96 && *str < 123)
		{
			*str -= 32;
		}
		str ++;
	}
	return (ptr_str);
}
